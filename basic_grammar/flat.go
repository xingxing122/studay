package main

import "fmt"

func main() {
	var num1 float32 = 3.14
	fmt.Println(num1)

	var num2 float32 = -3.14
	fmt.Println(num2)

	//定义字符类型的数据
	var c1 byte = 'a'
	fmt.Println(c1)

	var c2 byte = '6'
	fmt.Println(c2)
	//练习转义字符
	fmt.Println("aaaaa\bbbbbbb")
	fmt.Println("aaa\tbbb")

	var flag01 bool = true
	fmt.Println(flag01)

}
